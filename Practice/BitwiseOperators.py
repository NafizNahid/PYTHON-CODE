"""Bitwise operators

   COMPLEMENT (~)
   LEFT SHIFT (<<)
   RIGHT SHIFT (>>)
   AND (&)
   OR (|)
   XOR (^)"""
   

# Complement
~4 # output: -5; Formula: ~n = - n - 1

# Left shift
10 << 4 # output: 160; Formula: m << n = m*(2**n) [multiplies with the power of 2]

# Right shift
40 << 3 # output: 5; Formula: m >> n = m/(2**n) [devides by the power of 2]

